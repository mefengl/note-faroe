package main

// padEnd 函数用于在字符串末尾添加空格，直到字符串达到指定的长度 n。
// 如果原始字符串 s 的长度已经等于或大于 n，则函数直接返回原始字符串 s，不做任何修改。
// 如果原始字符串 s 的长度小于 n，函数会不断在 s 的末尾追加空格字符 (" ")，
// 直到 s 的长度等于 n 为止，然后返回填充后的字符串。
//
// 这个函数在需要对齐文本输出或者格式化字符串时可能很有用，例如在生成固定宽度的日志条目或表格时。
//
// 参数:
//   s (string): 需要进行填充的原始字符串。
//   n (int): 目标字符串的最小长度。
//
// 返回值:
//   string: 经过空格填充（如果需要）后达到指定长度 n 的字符串。
//
// 示例:
//   padEnd("hello", 10) // 返回 "hello     "
//   padEnd("world", 3)   // 返回 "world"
//   padEnd("", 5)        // 返回 "     "
func padEnd(s string, n int) string {
	// 使用一个循环来检查当前字符串 s 的长度是否小于目标长度 n
	for len(s) < n {
		// 如果长度小于 n，就在字符串末尾追加一个空格
		s += " "
	}
	// 当循环结束时，字符串 s 的长度已达到或超过 n，返回该字符串
	return s
}
